# >
print (7 > 3) # True
print (7 < 3) # False
print (7 >= 3) # True

# <
print(5 < 6) # True
print(5 < 5) # False
print(5 < 4) # False
print(5 <= 5) # True

# ==
print(5 == 5) # True
print(5 == 4) # False
print(5 == '5') # False
print('Apple' == "Apple") # True
print('Apple' == "apple") # False

# !=
print(5 != 5) # False
print(5 != 4) # True
print(5 != '5') # True